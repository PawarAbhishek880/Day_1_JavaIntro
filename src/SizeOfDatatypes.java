public class SizeOfDatatypes {
    static byte b;
    static short s;
    static int i;
    static float f;
    static long l;
    static double ld;
    static char c;
    static boolean bi;

    public static void main(String[] args) {
        System.out.println("Default value of primitive data types are:");
        System.out.println("Byte = " +b);
        System.out.println("Short = " + s);
        System.out.println("Integer = " +i);
        System.out.println("Float = " +f);
        System.out.println("Long = " +l);
        System.out.println("Double = " +ld);
        System.out.println("Char = " +c);
        System.out.println("Boolean = " +bi);
    }

}
